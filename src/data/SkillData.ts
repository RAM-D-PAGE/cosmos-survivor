// Helper to get localized description
export function getSkillDescription(skillId: string, lang: 'EN' | 'TH' = 'EN'): string {
    const skill = SKILL_DATA[skillId];
    if (!skill) return '';
    return lang === 'TH' ? (skill.descriptionTH || skill.description || '') : (skill.descriptionEN || skill.description || '');
}

